     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Requests\CreateStudent $request)
    {

        $student = new Student();
        $student->nombre =  $request->input('nombre');
        $student->apellidos =  $request->input('apellidos');
        $student->genero =  $request->input('genero');
        $student->fecha_nacimiento =  $request->input('fecha_nacimiento');
        $student->numero_celular =  $request->input('numero_celular');
        $student->status =  'Activo';
        $student->save();

        return  redirect('students');
    }

